export const KNOWLEDGE_BUDGET = 30000;

export function knowledgeFits(entries: Array<{ title: string; content: string }>): { used: number; dropped: number } {
  let budget = KNOWLEDGE_BUDGET;
  let dropped = 0;
  for (const e of entries) {
    const chunk = `\n\n### ${e.title}\n${e.content}`.slice(0, 12000);
    if (budget - chunk.length < 0) dropped++;
    else budget -= chunk.length;
  }
  return { used: KNOWLEDGE_BUDGET - budget, dropped };
}

