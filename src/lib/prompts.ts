export function knowledgeBlock(entries: Array<{ title: string; content: string }>): string {
  if (!entries.length) return "";
  let out = "\n\nПРАВИЛА КОМПАНИИ «ЛИНДЕН» (обязательны к исполнению; дополняют методику, но не заменяют и не отменяют её). Если правило противоречит твоему обычному выбору - действует правило:";
  let budget = KNOWLEDGE_BUDGET;
  let dropped = 0;
  for (const e of entries) {
    const chunk = `\n\n### ${e.title}\n${e.content}`.slice(0, 12000);
    if (budget - chunk.length < 0) {
      dropped++;
      continue;
    }
    out += chunk;
    budget -= chunk.length;
  }
  if (dropped > 0) out += `\n\n(не поместилось правил: ${dropped} - они не учтены)`;
  return out;
}

